    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String dni = jComboBox1.getSelectedItem().toString();
        try {
            try {
                Class.forName("org.sqlite.JDBC");
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Error!!!");
            }
            Connection conn = DriverManager.getConnection("jdbc:sqlite:agenda.sqlite");
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery("select * from contactos WHERE DNI='" + dni + "'");
            while (rs.next()) {
                jLabel7.setText(rs.getString("DNI"));
                jLabel8.setText(rs.getString("Nombre"));
                jLabel9.setText(rs.getString("Apellidos"));
                jLabel10.setText(rs.getString("Direccion"));
                jLabel11.setText(rs.getString("Telefono"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error!!!");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

